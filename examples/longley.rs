fn main() {
    let y: Col<f64> = Col::from_fn(Y.len(), |i| Y[i]);
    let x: Mat<f64> = Mat::from_fn(X.len(), 6, |i, j| X[i][j]);
    let res = Ols::new(y.as_ref(), x.as_ref())
        .fit()
        .expect("Longley fit")
        .with_names(vec![
            "const".into(), "deflator".into(), "gnp".into(),
            "unemp".into(), "armed".into(), "pop".into(), "year".into(),
        ]);
    println!("{res}");
}
